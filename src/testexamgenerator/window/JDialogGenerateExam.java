    private void jButtonGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGenerarActionPerformed
        if (jListUnits.getSelectedIndices().length<=0)
        {
            JOptionPane.showMessageDialog(this, "Select at least one unit on the left");
            return;
        }
        Integer numQuestions = (Integer)jSpinnerNum.getValue();
        if (numQuestions<=0)
        {
            JOptionPane.showMessageDialog(this,"Question number must be more than 0");
            return;
        }
        String fileName = jTextFieldFileName.getText();
        if ("".equals(fileName))
        {
            JOptionPane.showMessageDialog(this, "You should enter a valid output filename");
            return;
        }
        
        String template = (String)jComboBoxTemplates.getSelectedItem();
        if (BusinessLogic.generateExamenToPdf(numQuestions, jListUnits.getSelectedIndices(), fileName, template,jTextFieldSubtitle.getText()))
            JOptionPane.showMessageDialog(this, "Exam generation OK");
        
    }//GEN-LAST:event_jButtonGenerarActionPerformed

