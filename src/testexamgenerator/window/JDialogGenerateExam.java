    public JDialogGenerateExam(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        setLocationRelativeTo(null);
        initComponents();
        List<Unit> listUnit = BusinessLogic.getUnitList();
        DefaultListModel dlm = new DefaultListModel();
        for (Unit unit : listUnit)
            dlm.addElement(unit);
        jListUnits.setModel(dlm);
        
        //Charge templates
        File file = new File("templates");
        if (file.exists())
        {
            File[] templates = file.listFiles(new FilenameFilter() {

                @Override
                public boolean accept(File file, String name) {
                    if (name.endsWith(".jrxml") && !name.endsWith("answer_subreport.jrxml")) return true; 
                    else 
                        return false;
                }
            });
            
            DefaultComboBoxModel dcm = new DefaultComboBoxModel();
            for (File f : templates)
                dcm.addElement(f.getName());
            jComboBoxTemplates.setModel(dcm);
        }
    }

